package Semana5;

public class Operacion {
    int suma;
    int resta;
    int multiplicacion;
    int division;

    public void suma(int n1, int n2) {
        suma = n1 + n2;
    }
    public void resta(int n1, int n2) {
        resta = n1 - n2;
    }
    public void multiplicacion(int n1, int n2) {
        multiplicacion = n1 * n2;
    }
    public void division(int n1, int n2) {
        division = n1 / n2;
    }

}
